package apredo;

public class ejemploOperadoresAsig {
    static void main(String[] args) {
        int y = 26;

        y += 5; // a "y" se le suma ( en este caso 5)
        y -= 13; // a "y" se le resta (en este caso 13)
        y *= 7; // "y" se multiplica (7 en este caso)
        y /= 4; // "y" se divide entre ( este caso 4)

        System.out.println("resultado final : " + y);
    }
}
